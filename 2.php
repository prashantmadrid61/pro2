    <p class="abc">We understand that our reputation is judged by how we act. This is why our core values emphasise the importance of doing business with integrity and taking responsibility to do what is right and ethical at all times. These values underpin the Seagulf Airways Code of Business Conduct PDF file opens in a new window and are central to how we interact with our customers and suppliers.

