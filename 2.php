(Please note that the Ethics Line is not for customer complaints or general enquires. These should instead be channelled through our feedback channel where they will be dealt with by a member of our Guest Affairs team)</p><br>
    <h2>Ethics Line</h2>
    <b>What is Ethics Line?</b><br>
    It is Seagulf’s 24 hour global reporting service, operated by an independent company called Expolink Europe Ltd., where you can ask questions or raise concerns in confidence about matters of compliance with the Seagulf Code of Business Conduct or the law.<br><br>
    <b>Who can use Ethics Line?</b><br>
    Ethics Line is open to all employees, customers and other stakeholders to report their concerns. However, it cannot be used to report general customer or other complaints. These should be channelled through our dedicated feedback channel.<br><br>
    <b>How can I report a concern via Ethics Line?</b><br>
    You can submit a report in three ways:<br>

1.Call the Ethics Line phone number for your home country. Reports can be made in over 300 different languages. Click here opens in a new window for a list of numbers.<br>
2.Send an email with the details of your concern to ethicsline@expolink.co.uk<br>