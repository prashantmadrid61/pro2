    <p class="abc"><b>Armaguard Valuables Management (AVM)</b><br>
    Armaguard Valuables Management (AVM) is a joint venture between Etihad Airways and Armaguard Group opens in a new window, based in Abu Dhabi. AVM provides a full suite of secure logistics services to the global valuables market.

Armaguard’s security know-how, advanced technologies and valuables operational experience, combined with the network reach of one of the world’s largest and fastest growing airlines, allow a total and tailored solution platform.

