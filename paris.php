        <h2>About Paris</h2>
        <p class="aa">The most visited tourist destination in the world, with over 45 million visitors annually, it is very easy to arrive in Paris with huge expectations: of grand vistas, of budding writers in every cafe, of romance on the Seine and rude waiters.<br>

It is easy to see in Paris the city of love, or the city of lights. But it is also important to see in it the city that has seen 4 revolutions (to date), 18 kings named Louis and at least 2 occupying forces. Learn all about Paris with our helpful travel guide.
<br>
And this is not even all there is to know about Paris. Outside of the well beaten path of the Eiffel Tower and Notre Dame, there are 20 arrondissements to explore, each with their own character and version of Parisian life.</p><br>
        <p class="bb">
            <b>The Paris Inside Paris</b>
        <img src="top_6" height="300" width="300" align="right">
        The estimated 2.2 million inhabitants of Paris are all very proud to be Parisian, and so they should be. Which city has been more romanticized in literature and film? In fact there are 65 films (and counting) that star the Eiffel Tower as a significant feature. And that's just a small fraction of the number of films that merely feature the Eiffel Tower either as a symbol of French or European lifestyle or as an example of something beautiful.<br>

Despite its long, and often blood splattered history, Paris still retains the title of the most romantic city in the world. With the majestic sweeping boulevards and the regal architecture, it is hard not to be enchanted and seduced by this mysterious, copacetic city.</p>  <br>
        
        <p class="dd"><b>An International Melting Pot</b>
Of Paris' 2-million-strong population, around 20% are immigrants from all over the world making Paris one of the biggest multi-cultural centers in Europe. This meeting of cultures and cuisines means that Paris is always on (or at least near) the cutting edge in the worlds of food, art, music and fashion.

            Each neighborhood, or arrondissement, has its own story to tell about Paris. From the historical academia of the 5th arrondissement to the stylish and creative 6th and 7th; the monied, sleepy atmosphere of the 16th contrasted with the clash of cultures in the 18th, 19th and 20th arrondissements. Residents are frequently assessed, on first meeting at least, by their postcode, and as a consequence often develop a fierce sense of local pride. In our Paris guide you can read about each area of Paris and decide where you want to go. <br><br><br><br><br></p>
        <p class="cc">
        <b>A Paris Guide: the French Capital Inside and Out</b>
        <img src="top_25.jpg" height="300" width="300" align="left">
        If you are looking for international cuisine, innovative fashion, classical art or historical architecture, this city will deliver. A day appreciating classical art in the Louvre can be finished off listening to a underground band in the heart of the student scene in Oberkampf or the romantic atmosphere of the Eiffel Tower can be contrasted by a visit to the Catacombes to explore the city's bloody past.<br>

Our Paris guide will let you in on all the secrets you need to know about the city.

As the capital of France, Paris' transport system is clean, efficient and most importantly cheap. Other parts of the country are accessible from Paris in only a few hours, which makes for a large choice of day trips should you feel the need to escape the city. The Palais de Versailles and the Loire Valley are a must-see to truly understand Paris' decadent past whilst Disneyland Paris will entertain adults and children alike.<br>

A week, a month, a year isn't long enough to uncover all of Paris' treasures and you could spend a lifetime trying to pinpoint the subtle differences between the Right and Left Bank. But it would be a lifetime well spent. This city is populated by people, architecture, restaurants and nightlife that will seduce you and make you never want to leave.<br>

On all levels – historical, architectural, and cultural – there is something about Paris that will fascinate you.</p>
