        <h2>About Berlin</h2>
       <p class="aa">
        <img src="top_26" height="300" width="300" align="right">   
           <font size="30px">Berlin</font> is the capitol of Germany (since 1990 following German reunification) and has a population of 3.5M people.<br>

Berlin’s economy is mainly based on the service sector due to being one of the most popular touristic destinations in Europe.<br>

Other industries include IT, pharmaceuticals, renewable energy, biomedical engineering, and biotechnology.<br>

           Lately Berlin also is the epicenter for new and successful startups and sometimes is referred to as Silicon Allee (deriving from the original Silicon Valley in the U.S).</p>
        <p class="bb">
            <b>Climate in Berlin</b>
            <img src="top_27" height="300" width="300" align="left">
        Berlin climate can be best described as a humid continental climate.<br> In summertime it can get incredibly hot and the whole city seems to flee to the nearby lakes for some cooling down.<br>

If you are lucky you will catch a very hot and dry summer with many hours of sunshine a day.
<br>
After living in Berlin for several years now I can assure you that at least every 4-5 years the summers in Berlin are fantastic. That means on the other hand that the other summers can be very short and rainy.</p>
        <p class="cc">
            <b>Berlin Geography</b>
        Located in the eastern part of Germany Berlin is an important hub to the Eastern European states like Poland.<br>

Berlin is relatively flat and its biggest river is called Spree. In the Western Berlin borough Spandau the Spree meets the Havel.<br>

In this area there are many of the lakes like Wannsee and Tegeler See. But also if you take the S-Bahn direction East you will soon find yourself in the middle of great nature and lots of nice lakes that invite you to jump in and cool off during summertime.<br>
        </p>
        <p class="dd">
        <b>Berlin Neighbourhoods</b>
        Although many people would refer to Alexanderplatz being the city centre, there is no real city centre in Berlin. Rather Berlin has a lot of “Kieze” which are little communities of their own within Berlin.
